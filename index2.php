      <p>The Institute of Chartered Accountants of Nigeria (ICAN) was established by Act of Parliament No. 15 of 1965 to:

        <br>(i) determine what standards of knowledge and skill are to be attained by persons seeking to become members of the accountancy profession and to raise those standards from time to time as circumstances may permit;
        
        <br>(ii) secure in accordance with the provision of the Act, the establishment and maintenance of the registers of fellows, associates and registered accountants entitled to practice as accountants and auditors and to publish from time to time a list of those persons;
        
        <br>(iii) perform, through the Council of the Institute, all other functions conferred on it by the Act. As the foremost professional accounting body in the West African sub-region, in 1982, ICAN initiated and contributed significantly to the formation of the Association of Accounting Bodies in West Africa (ABWA). In fulfilling it mission, the Institute has over the years certified 38,000 Chartered Accountants who are engaged in value creation in various sectors of the economy. Apart from being in professional practice, ICAN members are engaged in the three tiers of public service as well as the private sector.</p>
